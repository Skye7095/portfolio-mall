package com.portfolio.mall.cart.model;

public class CartSum {
	private int totalAmount;
	private int totalProductPrice;
	private int totalDeliveryPrice;
	private int sum;
	public int getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(int totalAmount) {
		this.totalAmount = totalAmount;
	}
	public int getTotalProductPrice() {
		return totalProductPrice;
	}
	public void setTotalProductPrice(int totalProductPrice) {
		this.totalProductPrice = totalProductPrice;
	}
	public int getTotalDeliveryPrice() {
		return totalDeliveryPrice;
	}
	public void setTotalDeliveryPrice(int totalDeliveryPrice) {
		this.totalDeliveryPrice = totalDeliveryPrice;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}

	
	
}
